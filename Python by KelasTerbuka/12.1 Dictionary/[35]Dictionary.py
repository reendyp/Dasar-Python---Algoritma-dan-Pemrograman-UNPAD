# list -> array, mengakses dengan index
data_list = ["ucup","otong","dadang"]

# dictionary (dict) --> associative array
# identifier = key

data_dict = {
    "key" : "value", # value = bisa apa aja, boolean, str, int, float, list, bahkan dictionary juga bisa
    "nmbr" : 100,
    "list" : data_list
}
print(data_dict["key"])
print(data_dict["list"])
print(data_dict["nmbr"])